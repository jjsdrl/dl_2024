package Week02;

public class dome01 {
    public static void main(String[] args) {
        int i = 0;
        int j = 5;
        System.out.println(i++);
        System.out.println(++i);

        System.out.println(j--);
        System.out.println(--j);

        System.out.println(i++ + ++i + j-- + --j);
    }
}
